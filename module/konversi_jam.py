#fungsi konversi jam ke hari
def hari(jam):
    print('Konversi {} jam ke hari: {} hari'.format(jam,int(jam/24)))

#fungsi konversi jam ke menit
def menit(jam):
    print('Konversi {} jam ke menit: {} menit'.format(jam,jam * 60))

#fungsi konversi jam ke detik
def detik(jam):
    print('Konversi {} jam ke menit: {} detik'.format(jam,jam * 3600))